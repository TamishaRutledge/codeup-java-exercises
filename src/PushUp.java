public class PushUp {
    public static void main(String[] args){
        char letter = 'A';
        System.out.println("The defined input is: " + letter + " And that is a char value.");
        int PushUpNumber = 1000000;
        System.out.println("The defined input is: " + PushUpNumber + " And that is an int value.");
        String MyPushUpString = "This is a Java pushup!";
        System.out.println("The defined input is: " + MyPushUpString + " And that is a string value.");
        double TestNumber = 4.75;
        System.out.println("The defined input is: " + TestNumber + " And that is a double value.");
        boolean TestValue = true;
        System.out.println("The defined input is: " + TestValue + " And that is a boolean value.");
        float FloatVar = 3.98F;
        System.out.println("The defined input is: " + FloatVar + " And that is a float value.");
        int x = 5;
        System.out.println(x > 2 ? x < 4 ? 10 : 8 : 7);
    }
}
